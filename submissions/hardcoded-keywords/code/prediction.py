def get_event(text):
    tokens = text.split()
    possible_events = [event for token in tokens for event in event_keywords if token in event_keywords[event]]
    if len(possible_events) == 1:
        return int(possible_events[0])
    elif len(possible_events) > 1:
        return int(possible_events[random.randrange(0, len(possible_events))])
    else:
        # Didn't meet the rules, so just going to randomly guess
        possible_guesses = list(event_keywords.keys())
        guess_index = random.randrange(len(event_keywords.keys()))
        guess = possible_guesses[guess_index]
        return int(guess)


